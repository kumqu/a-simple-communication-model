# DES加密函数
def des_encrypt(msg_hex, key_hex):
    msg_bin = HexToBin(msg_hex)

    #子密玥生成器生成子密玥列表
    key_list = createKey(key_hex)

    #IP置换
    ip_k = ""
    for i in range(64):
        ip_k += msg_bin[ip[i]]
    L = ip_k[0: 32]
    R = ip_k[32: 64]

    for i in range(16):

        key_i = key_list[i]

        #E-扩展运算
        E_R = ""
        for j in range(48):
            E_R += R[E_table[j]]

        #密钥与E(R)异或
        K_E_R = xor(key_i, E_R, 48)

