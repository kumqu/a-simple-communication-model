# ECB加密模式
def ECB_encrypt(msg, key):
    ciper = ""

    while(len(msg) >= 16):
        ciper += des_encrypt(msg[0:16], key)
        msg = msg[16:]

    if len(msg) != 0:
        msg += (16-len(msg)) * '0'
        ciper += des_encrypt(msg, key)
    return ciper


# ECB解密模式
def ECB_decrypt(cpr, key):
    msg = ""
    while(len(cpr) > 0):
        msg += des_decrypt(cpr[0:16], key)
        cpr = cpr[16:]
    return msg
