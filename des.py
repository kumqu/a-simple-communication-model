# 十六进制转换为二进制
def HexToBin(hex):
    msg = ""
    for char in hex:
        temp = bin(int(char, 16))[2:]
        if len(temp) < 4:
            msg += '0' * (4-len(temp))
        msg += temp
    return msg


# 二进制转换为十六进制
def BinToHex(bin):
    msg = ""
    i = 0
    while(i < len(bin)):
        msg += hex(int(bin[i:i+4], 2))[2:]
        i += 4
    return msg


#异或运算
def xor(str1, str2, n):
    str = ""
    for i in range(n):
        if str1[i] != str2[i]:
            str += '1'
        else:
            str += '0'
    return str


